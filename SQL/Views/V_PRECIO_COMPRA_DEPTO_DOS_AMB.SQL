CREATE OR REPLACE VIEW PROYECTO_JUPI.CONSUMPTION_ZONE.V_PRECIO_COMPRA_DEPTO_DOS_AMB AS (
    SELECT DM.BARRIO AS Barrio_Compra, (PRECIO_USD * 50) AS PRECIO_COMPRA_DEPTO FROM PROYECTO_JUPI.CURATED_ZONE.L2_FACT_COMPRA_MT2 FCMT2
    INNER JOIN PROYECTO_JUPI.LANDING_ZONE.L1_DIM_BARRIOS DM
    ON FCMT2.ID_BARRIO = DM.ID_BARRIO
);

SELECT * FROM PROYECTO_JUPI.CONSUMPTION_ZONE.V_PRECIO_COMPRA_DEPTO_DOS_AMB;