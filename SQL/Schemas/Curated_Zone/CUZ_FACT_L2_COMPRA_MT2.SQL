CREATE
OR REPLACE TABLE PROYECTO_JUPI.CURATED_ZONE.L2_FACT_COMPRA_MT2 (
    ID NUMBER(38, 0) AUTOINCREMENT START 1 INCREMENT 1 PRIMARY KEY,
    ID_BARRIO NUMBER(38, 0),
    PRECIO_USD NUMBER(38, 0),
    FECHA DATE
);
INSERT INTO
    PROYECTO_JUPI.CURATED_ZONE.L2_FACT_COMPRA_MT2 (ID_BARRIO, PRECIO_USD, FECHA)
SELECT
    db.ID,
    pmcc.valor_mt2_usd,
    CURRENT_DATE
FROM
    PROYECTO_JUPI.LANDING_ZONE.L1_FACT_COMPRA_MT2 pmcc
    INNER JOIN PROYECTO_JUPI.LANDING_ZONE.L1_DIM_BARRIOS db ON pmcc.barrio = db.barrio;



